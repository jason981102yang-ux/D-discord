@bot.event
async def on_ready():
    print(f"登入成功：{bot.user}")
    if not check_timeout.is_running():
        check_timeout.start()

    @bot.event
    async def on_message(message):
        if message.author == bot.user:
            return
        content = message.content.strip()
        user_id = message.author.id
        now = datetime.datetime.now(datetime.timezone.utc)
        # 啟動指令
        if content == "繪名":
            active_users.add(user_id)
            user_last_active[user_id] = (message.channel, now)
            reply = ("幹嘛叫我")
            async with message.channel.typing():
                typing_time = min(max(len(reply) * 0.05, 1.0), 5.0)
                await asyncio.sleep(typing_time)
            await message.channel.send(reply)
            return
        # 關閉指令（可選）
        if content == "繪名 再見":
            active_users.discard(user_id)
            user_last_active.pop(user_id, None)
            reply = ("再見...記得休息!")
            async with message.channel.typing():

                typing_time = min(max(len(reply) * 0.05, 1.0), 5.0)

                await asyncio.sleep(typing_time)

            await message.channel.send(reply)
            return
        # 如果還沒啟動，就完全不回
        if user_id not in active_users:
            await bot.process_commands(message)
            return
        # 已啟動就更新最後互動時間
        user_last_active[user_id] = (message.channel, now)
        response_lines = []
        content_lower = content.lower()
        for topic, info in topics.items():
            if any(word in content_lower for word in info["keywords"]):
                response_lines = info["lines"]
                break
        if response_lines:
            reply = random.choice(response_lines)
        else:
            reply = random.choice(
                ["哼，你說的我聽不懂啦~", "這話題沒意思耶", "你在逗我嗎？", "真是的，隨便啦"])

        async with message.channel.typing():

            typing_time = min(max(len(reply) * 0.05, 1.0), 5.0)

            await asyncio.sleep(typing_time)

        await message.channel.send(reply)

        await bot.process_commands(message)


